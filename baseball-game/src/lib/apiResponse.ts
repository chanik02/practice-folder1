/**
 * API 응답 포맷 표준화
 */

export interface ApiResponse<T> {
  success: boolean;
  data?: T;
  error?: string;
  message?: string;
}

export interface ApiErrorResponse {
  success: false;
  error: string;
  message?: string;
}

export function successResponse<T>(data: T, message?: string): ApiResponse<T> {
  return {
    success: true,
    data,
    message,
  };
}

export function errorResponse(
  error: string,
  message?: string
): ApiErrorResponse {
  return {
    success: false,
    error,
    message,
  };
}

